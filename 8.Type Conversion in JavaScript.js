//Typecasting is termed as converting one type of data into the another type of data
let age=22;
let firstName="Krushna";
console.log(typeof(age));
console.log(typeof(firstName));
//Converting the number to stirng
let temp=age+"";
console.log(typeof(temp));
//Converting the string to number
let mystr="45";
console.log(typeof(mystr));
mystr=+mystr;//By adding + before string we can convert it to the number
console.log(typeof(mystr));

let myAge=34;
console.log(typeof(myAge));
myAge=String(myAge);
console.log(typeof(myAge));

let myName="67";
console.log(typeof(myName));
myName=Number(myName);
console.log(typeof(myName));
